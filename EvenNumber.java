package org.test;

public class EvenNumber {
	public static void main(String[] args) {
		int a=6;
		if (a%2 != 0) {
			System.out.println(a+" is a Odd Number");
		}
		else {
			System.out.println(a+" is a Even Number");
		}
		
		System.out.println(1);
		System.out.println(2);
		System.out.println(3);
	}

}
